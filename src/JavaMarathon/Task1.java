package JavaMarathon;

public class Task1 {
    public static void main(String[] args) {
        int i = 0;

        while (i < 10) {
            System.out.print("JAVA" + " ");
            i++;
        }

        System.out.print("------------------------");

        for (int a = 0; a < 10; a++) {
            System.out.print("JAVA" + " ");
        }
    }
}
